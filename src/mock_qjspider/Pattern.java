package mock_qjspider;

public class Pattern {
	
	public static void main(String[] args) {
		int star=0;
	for(int i=1;i<=4;i++) {
		  star++;
		  for(int j=1;j<=6;j++) {
			  if (star==1) { 
				  
				  System.out.print("*");
			  }
			  else {
			  	System.out.print(star);
			  }
		  }
		  System.out.println();
	}
	}
}
			
		
	
	
		
	


